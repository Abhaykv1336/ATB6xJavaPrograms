public class Concat {
    public static void main(String[] args){
        int a=10;
        int b=20;
        int c=30;
        String Name = "Abhay";
        System.out.println(a+b+c+Name); //60Abhay
        System.out.println(Name+a+b+c); //Abhay102030
        System.out.print("Hello \n \tAbhay");
    }
}